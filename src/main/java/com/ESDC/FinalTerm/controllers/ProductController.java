package com.ESDC.FinalTerm.controllers;

import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.ControllerAdvice;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestMapping;

import java.util.Arrays;
import java.util.List;

@Controller
@ControllerAdvice
@RequestMapping("/product")
public class ProductController {

    @GetMapping("/shoes")
    public String index(Model model){
        // Get the list of unique brands from your products (replace with your actual logic)
        List<String> uniqueBrands = Arrays.asList("Brand 1", "Nike", "Brand 3");

        // Add the brand options to the model
        model.addAttribute("brandOptions", uniqueBrands);

        return "/product-shoes";
    }
}
